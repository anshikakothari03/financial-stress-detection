last_month  = monthly_spend.groupby('UserID').last().reset_index()

user_features['growth_rate'] = (last_month['Amount'].values - first_month['Amount'].values) / \
                                first_month['Amount'].replace(0, np.nan).values

# Category ratio: essential vs non-essential
essential = ['Utilities','Rent','Food & Drink','Health & Fitness']

df_clean['IsEssential'] = df_clean['Category'].isin(essential).astype(int)

cat_stats = df_clean.groupby('UserID')['IsEssential'].mean()
user_features['pct_essential'] = user_features['UserID'].map(cat_stats)

# Category diversity index (Shannon entropy)
def shannon_entropy(x):
    p = x.value_counts(normalize=True)
    return -(p * np.log(p)).sum()

entropy = df_clean.groupby('UserID')['Category'].apply(shannon_entropy)
user_features['category_diversity'] = user_features['UserID'].map(entropy)

# Statistical features
stats_df = df_clean.groupby('UserID')['Amount'].agg(
    amount_skew = lambda x: skew(x),
    amount_kurtosis = lambda x: kurtosis(x),
    median_mean_ratio = lambda x: np.median(x) / (np.mean(x) if np.mean(x)!=0 else np.nan)
)

