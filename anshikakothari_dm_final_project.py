import time, pandas as pd
from mlxtend.frequent_patterns import apriori, fpgrowth

def time_method(method, matrix, min_support=0.2, max_len=None):
    t0 = time.time()
    if method=='fp':
        res = fpgrowth(matrix, min_support=min_support, use_colnames=True, max_len=max_len)
    else:
        res = apriori(matrix, min_support=min_support, use_colnames=True, max_len=max_len)
    return time.time()-t0, res

# Prune rare cols
col_freq = basket_matrix.sum(axis=0) / len(basket_matrix)
keep = col_freq[col_freq>=0.02].index
bm = basket_matrix[keep]

for s in (0.1, 0.15, 0.2):
    t_fp, r_fp = time_method('fp', bm, min_support=s, max_len=3)
    t_ap, r_ap = time_method('ap', bm, min_support=s, max_len=3)
    print(s, "FP:", round(t_fp,3), "n_itemsets", len(r_fp), " | Apriori:", round(t_ap,3), "n_itemsets", len(r_ap))

